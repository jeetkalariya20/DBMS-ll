--1. Select All CityID, CityName, Pincode, CityRemarks, StateID,StateName
	CREATE PROCEDURE PR_MST_CITY_SELECTALL
	AS
	BEGIN
	SELECT CITYID,
		   CITYNAME,
		   PINCODE,
		   CITYREMARKS,
		   MST_CITY.STATE_ID,
		   STATENAME
	FROM MST_CITY
	INNER JOIN MST_STATE

	ON MST_CITY.STATE_ID = MST_STATE.STATE_ID
	END
		--EXEC PR_MST_CITY_SELECTALL

--2. Insert
	CREATE PROCEDURE PR_MAST_CITY_INSERT_1
		@CITYID				INT,
		@CITYNAME			VARCHAR(250),
		@PINCODE			VARCHAR(6),
		@STATE_ID			INT,
		@CITYREMARKS		VARCHAR(500)
	AS 
	BEGIN
	INSERT INTO MST_CITY
				(
					CITYID,		
					CITYNAME,	
					PINCODE,	
					STATE_ID,	
					CITYREMARKS
				)
		VALUES
			(
				@CITYID,		
				@CITYNAME,	
				@PINCODE,	
				@STATE_ID,	
				@CITYREMARKS
			)
	END

		--EXEC PR_MAST_CITY_INSERT_1  parameters
--3. Update
	CREATE PROCEDURE PR_MST_CITY_UPDATE
		@CITYID				INT,
		@CITYNAME			VARCHAR(250),
		@PINCODE			VARCHAR(6),
		@STATE_ID			INT,
		@CITYREMARKS		VARCHAR(500)
	AS 
	BEGIN
	UPDATE MST_CITY
		SET  		
				CITYNAME=@CITYNAME,
				PINCODE=@PINCODE,	
				STATE_ID=@STATE_ID,	
				CITYREMARKS=@CITYREMARKS
		WHERE   CITYID=@CITYID
		END

		--EXEC PR_MST_CITY_UPDATE parameters


--4. Delete
	CREATE PROCEDURE PR_MST_CITY_DELETE
		@CITYID				INT
	AS 
	BEGIN
	DELETE FROM MST_CITY
		WHERE   CITYID=@CITYID
	END

		--EXEC PR_MST_CITY_DELETE parameter


