--6. SelectByPincodeStartWith360
	CREATE PROCEDURE PR_MST_CITY_STARTPINWITH360
	AS 
	BEGIN
	SELECT * FROM MST_CITY

	WHERE   PINCODE LIKE '360%'
	END
	
		--EXEC PR_MST_CITY_STARTPINWITH360


--7. SelectByCityNamePincode
	CREATE PROCEDURE PR_MST_CITY_CITYNAME_PINCODE
		@CITYNAME		VARCHAR(250),
		@PINCODE		VARCHAR(6)
	AS 
	BEGIN
	SELECT * FROM MST_CITY

	WHERE   CITYNAME=@CITYNAME	AND	PINCODE=@PINCODE
	END
		
		--EXEC PR_MST_CITY_CITYNAME_PINCODE parameters


